import numpy as np

arr1d = np.array([1, 2, 3, 4, 5])
print(arr1d)
print("Сума елементів:", np.sum(arr1d))
print("Середнє значення:", np.mean(arr1d))
print("Максимум:", np.max(arr1d))
print("Мінімум:", np.min(arr1d))
print()

arr2d = np.array([[1, 2, 3],
                  [4, 5, 6]])
print(arr2d)
print("Сума елементів:", np.sum(arr2d))
print("Середнє значення:", np.mean(arr2d))
print("Максимум:", np.max(arr2d))
print("Мінімум:", np.min(arr2d))
print()

